void print_effect(const char *str, const unsigned int delay_ms) {
  for (const char *c = str; *c != '\0'; ++c) {
    putchar(*c);
    fflush(stdout); // fais apparaitre directement le char
    sleep_ms(delay_ms);
  }
}

void print_text(const char *str) { print_effect(str, 10); }

